#!/bin/bash

# Проверяем, что переданы два аргумента
if [ $# -ne 2 ]; then
    echo "Ошибка: необходимо указать основание и высоту"
    echo "Использование: $0 <основание> <высота>"
    exit 1
fi

base=$1
height=$2

# Вычисляем площадь: 0.5 * основание * высоту
area=$(echo "scale=2; 0.5 * $base * $height" | bc)

echo "Площадь треугольника с основанием $base и высотой $height равна: $area"