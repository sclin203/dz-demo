#!/bin/bash

# Проверяем, что переданы три аргумента
if [ $# -ne 3 ]; then
    echo "Ошибка: необходимо указать три стороны треугольника"
    echo "Использование: $0 <сторона_a> <сторона_b> <сторона_c>"
    exit 1
fi

a=$1
b=$2
c=$3

# Проверяем существование треугольника (сумма любых двух сторон больше третьей)
if [ $(echo "$a + $b <= $c" | bc) -eq 1 ] || \
   [ $(echo "$a + $c <= $b" | bc) -eq 1 ] || \
   [ $(echo "$b + $c <= $a" | bc) -eq 1 ]; then
    echo "Ошибка: треугольника с такими сторонами не существует"
    exit 1
fi

# Вычисляем полупериметр
s=$(echo "scale=2; ($a + $b + $c) / 2" | bc)

# Вычисляем площадь по формуле Герона: √(s*(s-a)*(s-b)*(s-c))
# bc не имеет встроенной функции sqrt, используем формулу
area=$(echo "scale=2; sqrt($s * ($s - $a) * ($s - $b) * ($s - $c))" | bc)

echo "Площадь треугольника со сторонами $a, $b, $c равна: $area"