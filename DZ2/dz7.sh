#!/bin/bash

# Упражнение 1: Приветствие с аргументом
name="${1:-guest}"
echo "Привет, $name!"

# Упражнение 2: Запрос дня недели
echo "Какой сегодня день недели?"
read day
echo "Уже $day"

# Упражнение 4: Запрос числа
echo "Какое сегодня число?"
read number
