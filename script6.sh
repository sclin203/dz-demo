#!/bin/bash

# Проверяем, что передан один аргумент
if [ $# -ne 1 ]; then
    echo "Ошибка: необходимо указать одно число"
    echo "Использование: $0 <число>"
    exit 1
fi

number=$1

# Проверяем, является ли аргумент числом
if ! [[ "$number" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: аргумент должен быть целым числом"
    exit 1
fi

if [ $number -ge 0 ]; then
    echo "$number — положительное число"
else
    echo "$number — отрицательное число"
fi