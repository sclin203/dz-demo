#!/bin/bash

# Запрашиваем первое число
echo "Введите первое целое число:"
read num1

# Запрашиваем второе число
echo "Введите второе целое число:"
read num2

# Проверяем, что оба ввода являются целыми числами
if ! [[ "$num1" =~ ^-?[0-9]+$ ]] || ! [[ "$num2" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: оба значения должны быть целыми числами."
    exit 1
fi

# Проверяем, что второе число не равно нулю
if [ "$num2" -eq 0 ]; then
    echo "Ошибка: деление на ноль невозможно."
    exit 1
fi

# Упрощаем задачу: большее число будет первым, меньшее — вторым
if [ "$num1" -lt "$num2" ]; then
    # Меняем числа местами
    temp=$num1
    num1=$num2
    num2=$temp
    echo "Числа поменяны местами, чтобы большее было первым."
fi

# Вычисляем частное и остаток
quotient=$((num1 / num2))
remainder=$((num1 % num2))

# Выводим результат
echo "Для чисел $num1 и $num2:"
echo "Частное: $quotient"
echo "Остаток: $remainder"